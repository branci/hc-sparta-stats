CREATE TABLE "MATCH"(
    "MATCH_ID" BIGINT PRIMARY KEY,
    "DATE" DATE,
    "OPPONENT" VARCHAR(30),
    "SPARTA_GOALS" INTEGER,
    "OPPONET_GOALS" INTEGER,
    "SPARTA_SHOTS" INTEGER,
    "OPPONET_SHOTS" INTEGER,
    "SPARTA_PENALTY" INTEGER,
    "OPPONET_PENALTY" INTEGER,
    "HOME" VARCHAR(30),
    "BEST_PLAYER" VARCHAR(30),
    "PLAYOFF" VARCHAR(20),
    "SEASON" INTEGER NOT NULL);

CREATE TABLE "PLAYERS" (
    "PLAYERID" INT PRIMARY KEY,
    "NAME" VARCHAR(30) NOT NULL,
    "AGE" INTEGER,
    "HEIGHT" INTEGER,
    "WEIGHT" INTEGER,
    "PLAYERNUM" INTEGER,
    "POSITION" INTEGER);

CREATE TABLE "STATS" (
    PLAYERID INT,
    MATCHID INT,
    GOALS INT,
    ASSISTS INT,
    PENALTY_MINUTES INT,
    SHOTS INT,
    HITS INT);

CREATE TABLE "GOAL" (
    "GOAL_ID" BIGINT ,
    "MATCH_ID" BIGINT NOT NULL,
    "PLAYER_NAME" VARCHAR(20) NOT NULL,
    "MINUTE" INTEGER NOT NULL,
    "PLAYERID" INT);

CREATE TABLE "ASSIST" (
    "ASSIST_ID" BIGINT PRIMARY KEY,
    "MATCH_ID" BIGINT NOT NULL ,
    "PLAYER_NAME" VARCHAR(20) NOT NULL,
    "GOAL_ID" BIGINT NOT NULL,
    "PLAYERID" INT);

CREATE TABLE "PLAYED" (
    "MATCH_ID" BIGINT NOT NULL,
    "PLAYER_NAME" VARCHAR(20),
    "PLAYERID" INT);
